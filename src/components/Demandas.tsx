export type DemandStatus = 'backlog' | 'a_fazer' | 'em_andamento' | 'aguardando' | 'revisao' | 'concluido';
export type DemandPriority = 'baixa' | 'media' | 'alta' | 'urgente';
export type RecurrenceInterval = 'diaria' | 'semanal' | 'quinzenal' | 'mensal';

