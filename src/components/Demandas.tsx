const todayStr = () => new Date().toISOString().split('T')[0];

const diffDaysFromToday = (dateStr: string): number => {
  const today = new Date(todayStr() + 'T00:00:00');
  const target = new Date(dateStr + 'T00:00:00');
  return Math.floor((today.getTime() - target.getTime()) / 86400000);
};

const advanceDate = (dateStr: string, interval: RecurrenceInterval): string => {
  const d = new Date(dateStr + 'T00:00:00');
  switch (interval) {
    case 'diaria': d.setDate(d.getDate() + 1); break;
    case 'semanal': d.setDate(d.getDate() + 7); break;
    case 'quinzenal': d.setDate(d.getDate() + 14); break;
    case 'mensal': d.setMonth(d.getMonth() + 1); break;
  }
  return d.toISOString().split('T')[0];
};

const formatDate = (dateStr?: string) => {
  if (!dateStr) return '';
  const parts = dateStr.split('-');
  if (parts.length !== 3) return dateStr;
  return `${parts[2]}/${parts[1]}/${parts[0]}`;
};

